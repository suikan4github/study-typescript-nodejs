
// String variable
let string_variable: string = 'My string in variable';
console.log(string_variable);

// Type predication
let string_variable2 = 'Sting variable with type predication';
console.log(string_variable2);

// Number variable
let number_variable = 3.14;
console.log(number_variable);

// Boolean variable
let boolean_variable = true;
console.log(boolean_variable);

// Assignment to variable
number_variable = 2.718;
console.log(number_variable);

// Assigning the different type is not allowed. 
// number_variable = '2.718';

// Assigning new value to the constant is not allowed. 
const number_variable2 = 1;
// number_variable2 = 2;

// variable defined by var is not recommended. It is legacy.
var number_variable3 = 3;
console.log(number_variable3);
